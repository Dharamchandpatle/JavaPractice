class exatype{
  public static void main(String args[])
  {
    byte b = 50;
    int i = 123456789;
    long l = 1234567654321L;
    float f = 3.142f;
    short s = (short)b;
    short s1 = (short)i;
    float n = (float)l;
    int m = (int)f;
System.out.println("type converted ");
System.out.println("(short)b = "+s);
System.out.println("( short)i = "+s1);
System.out.println("(float)l = "+n);
System.out.println("(int)f = "+m);


    }
}